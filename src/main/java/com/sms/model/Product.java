package com.sms.model;

public class Product {
	private int productId;
	private String productName;
	private int productCategory;
	private int productQuantity;
	private int productCostPrice;
	private int productSellingPrice;
	private int productOrdersMade;
	private int productEarnings;
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public int getProductCategory() {
		return productCategory;
	}
	public void setProductCategory(int productCategory) {
		this.productCategory = productCategory;
	}
	public int getProductQuantity() {
		return productQuantity;
	}
	public void setProductQuantity(int productQuantity) {
		this.productQuantity = productQuantity;
	}
	public int getProductCostPrice() {
		return productCostPrice;
	}
	public void setProductCostPrice(int productCostPrice) {
		this.productCostPrice = productCostPrice;
	}
	public int getProductSellingPrice() {
		return productSellingPrice;
	}
	public void setProductSellingPrice(int productSellingPrice) {
		this.productSellingPrice = productSellingPrice;
	}
	public int getProductOrdersMade() {
		return productOrdersMade;
	}
	public void setProductOrdersMade(int productOrdersMade) {
		this.productOrdersMade = productOrdersMade;
	}
	public int getProductEarnings() {
		return productEarnings;
	}
	public void setProductEarnings(int productEarnings) {
		this.productEarnings = productEarnings;
	}
	

}
